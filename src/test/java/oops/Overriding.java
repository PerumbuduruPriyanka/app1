package oops;

public class Overriding {
	void ShowA() {
		System.out.println("priyanka");
	}
	void ShowB() {
		System.out.println("haripriya");
	
	}
	void ShowC() {
		System.out.println("madhavi");
		
	}

	public static void main(String[] args) {
		Overriding r=new Overriding();
		r.ShowA();
		r.ShowB();
		r.ShowC();
		

	}

}
